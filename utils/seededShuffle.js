function seededShuffle(array, seedStr) {
  const arr = array.slice();
  const rand = mulberry32(fnv1a(String(seedStr)));

  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

